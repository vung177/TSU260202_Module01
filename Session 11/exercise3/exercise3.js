let checkNumber = Number(prompt('Enter a number: '));
switch (checkNumber) {
  case 1:
    console.log('This is "Số Một"');
    break;
  case 2:
    console.log('This is "Số Hai"');
    break;
  case 3:
    console.log('This is "Số Ba"');
    break;
  case 4:
    console.log('This is "Số Bốn"');
    break;
  case 5:
    console.log('This is "Số Năm"');
    break;
  case 6:
    console.log('This is "Số Sáu"');
    break;
  case 7:
    console.log('This is "Số Bảy"');
    break;
  case 8:
    console.log('This is "Số Tám"');
    break;
  case 9:
    console.log('This is "Số Chín"');
    break;
  case 0:
    console.log('This is "Số Không"');
    break;
  default:
    console.log('Từ từ, bình tĩnh, chưa làm code cho số lớn hơn 9');
    break;
}
